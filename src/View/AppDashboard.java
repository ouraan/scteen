    private void btnChangePasswordMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnChangePasswordMouseClicked
        try{
            java.sql.Connection conn=(Connection)Koneksi.configDB();
            java.sql.Statement stm=conn.createStatement();
            String sql = "SELECT * FROM data_siswa";
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                as.setPassword(res.getString("password_siswa"));
                if(tfOldPassword.getText().equals(as.getPassword())){
                    try{
                        String sql_update_pass = "UPDATE data_siswa SET password_siswa=? WHERE password_siswa = '"+tfOldPassword.getText()+"'";
                        java.sql.PreparedStatement pst=conn.prepareStatement(sql_update_pass);
                        as.setPassword(tfNewPassword.getText());
                        pst.setString(1, as.getPassword());
                        if(tfConfirmPassword.getText().equals(as.getPassword())){
                            pst.executeUpdate();
                            tfOldPassword.setText("");
                            tfNewPassword.setText("");
                            tfConfirmPassword.setText("");
                            JOptionPane.showMessageDialog(null, "Password has been changed successfully.", "Message", JOptionPane.INFORMATION_MESSAGE);
                        }
                        else{
                            JOptionPane.showMessageDialog(null, "Please confirm the correct password.", "Error", JOptionPane.ERROR_MESSAGE);
                        }
                    }
                    catch(Exception e){
                        JOptionPane.showMessageDialog(null, "Failed to change password.\n" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "\"Failed to change password.\n" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }    
    }//GEN-LAST:event_btnChangePasswordMouseClicked

